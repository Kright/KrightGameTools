package me.kright.gametools.pga.codegen.scala.pga3d

import Pga3dScalaAlgebra.pgaClasses
import me.kright.gametools.pga.codegen.common.{FileContent, GeneratedFileSystem, RealFileSystem}
import me.kright.gametools.pga.codegen.scala.common.OperationsReference

import java.nio.file.{Files, Path}


@main
def runScalaCodeGen(): Unit = {
  runScala3dCodeGen(RealFileSystem())
}

def runScala3dCodeGen(fs: GeneratedFileSystem): Unit = {
  val packageDir = Path.of("pga3d/shared/src/main/scala/me/kright/gametools/pga3d")
  assert(Files.exists(packageDir))

  for (cls <- pgaClasses if cls.shouldBeGenerated) {
    cls.writeToFile(packageDir, fs)
  }

  ScalaMatrixCodeGen().writeToFile(packageDir, fs)

  writeQuaternionAliasFile(packageDir, fs)

  OperationsReference.writeToFile(Path.of("pga3d/operations.md"), fs)(using Pga3dScalaAlgebra)
}

/** Pga3dQuaternion was renamed to Pga3dRotor; this file keeps the old name usable as a backward-compatible alias. */
private def writeQuaternionAliasFile(packageDir: Path, fs: GeneratedFileSystem): Unit = {
  val content =
    """package me.kright.gametools.pga3d
      |
      |// Generated by pgaNdCodeGen (me.kright.gametools.pga.codegen.scala.pga3d.runScalaCodeGen).
      |// Do not edit by hand: change the generator and re-run it.
      |// Backward-compatible alias: Pga3dQuaternion was renamed to Pga3dRotor.
      |type Pga3dQuaternion = Pga3dRotor
      |val Pga3dQuaternion: Pga3dRotor.type = Pga3dRotor
      |""".stripMargin

  fs.write(FileContent(packageDir.resolve("Pga3dQuaternion.scala"), content))
}
